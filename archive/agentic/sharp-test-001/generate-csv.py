def write_csv(rows, fp):
    with open(fp, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=COLUMNS, quoting=csv.QUOTE_ALL)
        writer.writeheader()
        writer.writerows(rows)


def validate_csv(fp):
    with open(fp, "r", encoding="utf-8") as f:
        rows = list(csv.reader(f))
    errors = []
    bad_cols = [(i + 1, len(r)) for i, r in enumerate(rows) if len(r) != 15]
    if bad_cols:
        errors.append("Column count errors: {}".format(bad_cols))
    comma_cells = []
    nl_wrong = []
    for i, r in enumerate(rows[1:], 2):
        for j, cell in enumerate(r):
            if "," in cell:
                comma_cells.append((i, COLUMNS[j], cell[:40]))
            if ("\n" in cell or "\r" in cell) and j not in (STEPS_COL, EXPECTED_COL):
                nl_wrong.append((i, COLUMNS[j]))
    if comma_cells:
        errors.append("Embedded commas: {}".format(len(comma_cells)))
        for row, col, val in comma_cells[:5]:
            errors.append("  Row {} {}: {}...".format(row, col, val))
    if nl_wrong:
        errors.append("Newlines in wrong columns: {}".format(nl_wrong))
    if errors:
        print("FAIL:")
        for e in errors:
            print("  {}".format(e))
        return False

    total = len(rows) - 1
    print("\nPASS: {} rows / 15 cols / no commas / newlines only in Steps+Expected".format(total))
    sections = {}
    for r in rows[1:]:
        sec = r[0]
        t = r[10]
        sections.setdefault(sec, {"Smoke Test": 0, "Sanity Test": 0, "Regression Test": 0, "total": 0})
        sections[sec][t] += 1
        sections[sec]["total"] += 1
    print("\n{:<50} {:>4} {:>4} {:>4} {:>4}".format("Section", "Tot", "Smk", "San", "Reg"))
    print("-" * 70)
    for sec, c in sorted(sections.items()):
        print("{:<50} {:>4} {:>4} {:>4} {:>4}".format(
            sec, c["total"], c["Smoke Test"], c["Sanity Test"], c["Regression Test"]))
    print("-" * 70)
    print("{:<50} {:>4}".format("TOTAL", total))
    pris = {}
    types = {}
    for r in rows[1:]:
        pris[r[11]] = pris.get(r[11], 0) + 1
        types[r[10]] = types.get(r[10], 0) + 1
    print("\nPriorities: {}".format(pris))
    print("Types: {}".format(types))
    return True


if __name__ == "__main__":
    write_csv(ALL, OUTPUT)
