#!/usr/bin/env python3
"""
Generate TestRail CSV for EkoAI Scheduled Jobs test cases.

All test cases defined inline with:
- Check/Verify title style
- Multi-line Steps and Expected Result (real newlines for TestRail rendering)
- Explicit positive/negative/edge categorization per group
- csv.QUOTE_ALL for safe quoting
"""
import csv
import re

OUTPUT = "ekoai-scheduled-jobs-testcases.csv"

COLUMNS = [
    "Section", "Role", "Channel", "Title", "Test Data",
    "Preconditions", "Steps", "Expected Result", "Platform",
    "TestMethod", "Type", "P", "References", "Release version",
    "QA Responsibility",
]

STEPS_COL = 6
EXPECTED_COL = 7


def get_type(cat, pri):
    if cat == "positive" and pri == "P1":
        return "Smoke Test"
    if cat == "positive":
        return "Sanity Test"
    return "Regression Test"


def sanitize(value, allow_newlines=False):
    v = value.replace(",", " /")
    if not allow_newlines:
        v = v.replace("\n", " ").replace("\r", " ")
        v = re.sub(r"\s+", " ", v)
    else:
        lines = v.split("\n")
        lines = [re.sub(r"\s+", " ", l).strip() for l in lines]
        v = "\n".join(lines)
    return v.strip()


def build_rows(section, role, channel, platform, preconditions, cases):
    rows = []
    for c in cases:
        cat, pri, title, data, pre, steps, expected, ref = c
        if not pre:
            pre = preconditions
        rows.append({
            "Section": section,
            "Role": role,
            "Channel": channel,
            "Title": sanitize(title),
            "Test Data": sanitize(data),
            "Preconditions": sanitize(pre),
            "Steps": sanitize(steps, allow_newlines=True),
            "Expected Result": sanitize(expected, allow_newlines=True),
            "Platform": platform,
            "TestMethod": "Manual",
            "Type": get_type(cat, pri),
            "P": pri,
            "References": sanitize(ref),
            "Release version": "Eko 18.0",
            "QA Responsibility": "Sharp",
        })
    return rows


